package com.lm.lib_common.model;

public class ErrorBean {

    /**
     * code : 401
     * message : 未授权
     */

    private int code;
    private String message;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
